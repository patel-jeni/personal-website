import clsx from 'clsx'

interface IconProps {
  children: ReactNode
  size?: number
  className?: string
  'aria-label'?: string
  'aria-hidden'?: boolean
}

export function Icon({
  children,
  size = 24,
  className,
  'aria-label': ariaLabel,
  'aria-hidden': ariaHidden = !ariaLabel,
}: IconProps) {
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth="2"
      strokeLinecap="round"
      strokeLinejoin="round"
      className={clsx('inline-block', className)}
      aria-label={ariaLabel}
      aria-hidden={ariaHidden}
      role={ariaLabel ? 'img' : undefined}
    >
      {children}
    </svg>
  )
}

